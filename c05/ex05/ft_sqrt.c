int	ft_sqrt(int nb)

{
	long	i;

	i = 1;
	if (nb > 2147395600)
	{
		return (0);
	}
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
